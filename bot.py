def fmt_size(b):
    if b >= 1024**3: return f"{b/1024**3:.1f}GB"
    return f"{b/1024**2:.1f}MB"

def fmt_speed(kbs):
    if kbs >= 1024: return f"{kbs/1024:.1f}MB/s"
    return f"{kbs:.0f}KB/s"

