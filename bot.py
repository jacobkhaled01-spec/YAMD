def split_video(filepath: str, max_part_size: int = MAX_SIZE) -> Optional[List[str]]:
    if not shutil.which("ffmpeg") or not shutil.which("ffprobe"):
        return None

    base = os.path.splitext(filepath)[0]
    pattern = f"{base}_part%03d.mp4"

    try:
        dur_raw = subprocess.run(
            [
                "ffprobe",
                "-v",
                "quiet",
                "-show_entries",
                "format=duration",
                "-of",
                "csv=p=0",
                filepath,
            ],
            capture_output=True,
            text=True,
            timeout=60,
        ).stdout.strip()
        dur = float(dur_raw)
    except Exception:
        return None

    if dur <= 0:
        return None

    total_size = os.path.getsize(filepath)
    if total_size <= max_part_size:
        return [filepath]

    parts_count = math.ceil(total_size / max_part_size)
    seg = max(1, math.ceil(dur / parts_count) + 1)

    try:
        subprocess.run(
            [
                "ffmpeg",
                "-y",
                "-i",
                filepath,
                "-c",
                "copy",
                "-map",
                "0",
                "-f",
                "segment",
                "-segment_time",
                str(seg),
                "-reset_timestamps",
                "1",
                "-segment_format",
                "mp4",
                pattern,
            ],
            check=True,
            capture_output=True,
            text=True,
            timeout=600,
        )
    except Exception:
        return None

    return [str(p) for p in sorted(Path(base).parent.glob(f"{Path(filepath).stem}_part*.mp4"))]

