def _setup_db():
    db.execute("""CREATE TABLE IF NOT EXISTS users(
        uid INTEGER PRIMARY KEY, username TEXT, fname TEXT,
        seen TEXT, active TEXT, cnt INTEGER DEFAULT 0)""")
