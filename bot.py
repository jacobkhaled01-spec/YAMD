def split_video(filepath, max_part_size=MAX_SIZE):
    if not HAS_FFMPEG: return None
    base = os.path.splitext(filepath)[0]
    pattern = f"{base}_part%03d.mp4"
    try:
        dur = float(subprocess.run(["ffprobe","-v","quiet","-show_entries","format=duration","-of","csv=p=0",filepath], capture_output=True,text=True).stdout.strip())
    except: return None
    if not dur: return None
    total_size = os.path.getsize(filepath)
    parts_count = math.ceil(total_size / max_part_size)
    seg = math.ceil(dur / parts_count) + 1
    subprocess.run(["ffmpeg","-i",filepath,"-c","copy","-map","0","-f","segment","-segment_time",str(seg),"-reset_timestamps","1","-segment_format","mp4",pattern], check=True, capture_output=True)
    return [str(p) for p in sorted(Path(base).parent.glob(f"{Path(filepath).stem}_part*.mp4"))]

